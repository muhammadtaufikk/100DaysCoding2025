package day66coding;
import java.util.Scanner;

public class Day66coding {
    public static void main(String[] args) {
        Scanner l = new Scanner(System.in);
        System.out.print("Masukkan tinggi (n): ");
        int n = l.nextInt();

        for (int i = 0; i < n; i++) {
            System.out.println("*");
        }
    }
}
